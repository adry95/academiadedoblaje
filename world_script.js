// Funcion de mostrar las fotos
function photo_display(_folder, total_image_number) {
    var codeBlock = "";
    var comentario = [];
    // Directorio del que coger las fotos
    if (_folder == "pueblokebab") {
        comentario = comentario_pueblokebab;
    } else if (_folder == "ovatimmy") {
        comentario = comentario_ovatimmy;
    } else if (_folder == "vallekebab") {
        comentario = comentario_vallekebab;
    }
    // Bucle que muestra las fotos
    for (var i=1; i<total_image_number+1; i++) {
        codeBlock +='<a href="fotos-'+_folder+'/foto_'+i+'.png" target="_blank">' +
                    '<div class="image">' +
                    '<img class="image__img" src="fotos-'+_folder+'/foto_'+i+'.png">';
                    if (comentario[i] !== undefined) {
                        codeBlock +='<div class="image__overlay">' +
                                    '<div class="image__title">'+comentario[i]+'</div>' +
                                    '</div>' +
                                    '</div></a>';
                    }
                    else {
                        codeBlock += '</div></a>';
                    }
        document.getElementById("photos").innerHTML = codeBlock;
    }    
}
